def evaluate(gs):
    """
    Hàm đánh giá chính — chỉ tính material + PST.
    Terminal nodes (chiếu hết, hòa) được xử lý trong search(), không gọi ở đây.
    Dương (+) là ưu thế cho Trắng. Âm (-) là ưu thế cho Đen.
    """
    score = 0
    bitboards = gs.board.bitboards

    for name, bb in bitboards.items():
        piece_type = name[1]
        is_white = name.startswith('w')

        val = PIECE_VALUES[piece_type]
        pst_table = PST[piece_type]

        temp_bb = bb
        while temp_bb:
            square = (temp_bb & -temp_bb).bit_length() - 1

            if is_white:
                score += val + pst_table[square ^ 56]
            else:
                score -= (val + pst_table[square])

            temp_bb &= temp_bb - 1

    if gs.castling_rights['wK']:
        score += 15
    if gs.castling_rights['wQ']:
        score += 15

    if gs.castling_rights['bK']:
        score -= 15
    if gs.castling_rights['bQ']:
        score -= 15

    return score
